        <ContentSide />
      </div>
      <div class="c-block__login">
        <LoginSide />
      </div>
    </div>
  </div>
</template>

<script>
import ContentSide from '~/components/login/ContentSide'
import LoginSide from '~/components/login/LoginSide'

export default {
  name: 'Login',
  components: {
    ContentSide,
    LoginSide
  },
  data() {
    return {
      viewportWidth: 0
    }
  },
  beforeMount() {
    window.addEventListener('resize', this.onWindowSizeChange)
  },
  mounted() {
    this.viewportWidth = this.getWidth()
    if (this.viewportWidth < 900) {
      this.pinInputHeight = '56px'
    }
  },
  destroyed() {
    window.removeEventListener('resize', this.onWindowSizeChange)
  },
  methods: {
    onWindowSizeChange() {
      this.viewportWidth = this.getWidth()
    },
    getWidth() {
      return Math.max(
        document.documentElement.clientWidth,
        window.innerWidth || 0
      )
    }
  }
}
</script>

